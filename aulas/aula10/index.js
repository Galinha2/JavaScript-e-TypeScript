/*
Operadores Aritméticos:
+ Adição
- Subtração
* Multiplicação
/ Divisão
** Elevádo
% Resto da Divisão
*/

let num1 = 12;
const num2 = 27;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);

console.log(++num1)
console.log(num1++)
console.log(--num1)
console.log(num1--)